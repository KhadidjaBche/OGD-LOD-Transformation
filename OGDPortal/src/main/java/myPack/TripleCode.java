package myPack;


public class TripleCode {

		public int sujet;
		public int predicat;
		public int objet;


		public  TripleCode(){
	
			sujet=this.sujet;
			predicat=this.predicat;
			objet=this.objet;
		}

		public TripleCode (int s,int p,int o){
			
			this.sujet=s;
			this.predicat=p;
			this.objet=o; 	
		}
}
