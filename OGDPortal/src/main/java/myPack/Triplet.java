package myPack;


public class Triplet {
	
	private String s;
	private String p;
	private String o;
	
	public  Triplet(){
		
	}
	
	public  Triplet(String s, String p, String o){	
		s=this.s;
		p=this.p;
		o=this.o;
	}
	
	public String getS(){
		return this.s;
	}
	public void setS(String S){
		this.s = S;
	}
	
	public String getP(){
		return this.p;
	}
	public void setP(String P){
		this.p = P;
	}
	
	public String getO(){
		return this.o;
	}
	public void setO(String O){
		this.o = O;
	}

}


